
 function showWrite(id1, id2){
     var getDiv1 = document.getElementById(id1);
     var getDiv2 = document.getElementById(id2);
     getDiv1.style.display = "block";
     getDiv2.style.display = "block";
 }
 
 function hideWrite(id1, id2) {
     var getDiv1 = document.getElementById(id1);
     var getDiv2 = document.getElementById(id2);
     getDiv1.style.display = "none";
     getDiv2.style.display = "none";
 }